#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	c;
	int	n;
	int	r;

	c = 0;
	n = 1;
	r = 0;
	while (nptr[c] == ' ' || (nptr[c] >= 9 && nptr[c] <= 13))
		c++;
	if (nptr[c] == '-' || nptr[c] == '+')
	{
		if (nptr[c] == '-')
			n *= -1;
		c++;
	}
	while (nptr[c] >= '0' && nptr[c] <= '9')
	{
		r = (nptr[c] - '0') + (r * 10);
		c++;
	}
	return (r * n);
}
