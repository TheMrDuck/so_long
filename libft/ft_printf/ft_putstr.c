#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		return (write(1, "(null)", 6));
	}
	while (*str)
	{
		write (1, str, 1);
		str++;
		i++;
	}
	return (i);
}
